ensimmäinen = float(input("Anna ensimmäinen arvo "))
toinen = float(input("Anna toinen arvo "))
kolmas = float(input("Anna kolmas arvo "))
summa = ensimmäinen + toinen + kolmas
tulo = ensimmäinen * toinen * kolmas
keskiarvo = (ensimmäinen + toinen + kolmas) / 3
print(f"Arvojen summa on {summa}, tulo on {tulo} ja keskiarvo on {keskiarvo}")